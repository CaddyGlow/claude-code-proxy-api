#!/usr/bin/env node
/**
 * Version synchronization script for dashboard
 *
 * Reads version from parent project's hatch-vcs generated _version.py file
 * and updates package.json to maintain version consistency across the project.
 */

import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const DASHBOARD_ROOT = join(__dirname, '..');
const PROJECT_ROOT = join(DASHBOARD_ROOT, '..');
const VERSION_FILE = join(PROJECT_ROOT, 'ccproxy', '_version.py');
const PACKAGE_JSON = join(DASHBOARD_ROOT, 'package.json');

/**
 * Extract version from Python _version.py file generated by hatch-vcs
 */
function extractVersionFromPython() {
  try {
    const versionContent = readFileSync(VERSION_FILE, 'utf8');

    // Match patterns like: __version__ = version = '0.1.dev143+g3eaf15b.d19800101'
    const versionMatch = versionContent.match(/__version__\s*=\s*version\s*=\s*['"](.*?)['"]/) ||
                        versionContent.match(/version\s*=\s*['"](.*?)['"]/) ||
                        versionContent.match(/__version__\s*=\s*['"](.*?)[''"]/);

    if (versionMatch) {
      return versionMatch[1];
    }

    throw new Error('Could not parse version from _version.py');
  } catch (error) {
    if (error.code === 'ENOENT') {
      console.warn('Warning: _version.py not found. This is expected during development.');
      console.warn('Run `uv run python -c "import ccproxy; print(ccproxy.__version__)"` to generate it.');
      return '0.0.1-dev';
    }
    throw error;
  }
}

/**
 * Format hatch-vcs version for NPM semantic versioning
 * Converts formats like:
 * - '0.1.dev143+g3eaf15b.d19800101' -> '0.1.0-dev143'
 * - '1.2.3' -> '1.2.3'
 * - '1.2.3rc1' -> '1.2.3-rc1'
 */
function formatForNpm(hatchVersion) {
  // Handle development versions: 0.1.dev143+g3eaf15b.d19800101
  const devMatch = hatchVersion.match(/^(\d+\.\d+)\.dev(\d+)/);
  if (devMatch) {
    const [, baseVersion, devNumber] = devMatch;
    return `${baseVersion}.0-dev${devNumber}`;
  }

  // Handle release candidates: 1.2.3rc1
  const rcMatch = hatchVersion.match(/^(\d+\.\d+\.\d+)rc(\d+)/);
  if (rcMatch) {
    const [, baseVersion, rcNumber] = rcMatch;
    return `${baseVersion}-rc${rcNumber}`;
  }

  // Handle alpha/beta versions
  const preMatch = hatchVersion.match(/^(\d+\.\d+\.\d+)(a|b)(\d+)/);
  if (preMatch) {
    const [, baseVersion, preType, preNumber] = preMatch;
    const preTypeMap = { 'a': 'alpha', 'b': 'beta' };
    return `${baseVersion}-${preTypeMap[preType]}${preNumber}`;
  }

  // Handle standard semver: 1.2.3
  const semverMatch = hatchVersion.match(/^(\d+\.\d+\.\d+)$/);
  if (semverMatch) {
    return semverMatch[1];
  }

  // Handle two-part versions: 0.1
  const twoPartMatch = hatchVersion.match(/^(\d+\.\d+)$/);
  if (twoPartMatch) {
    return `${twoPartMatch[1]}.0`;
  }

  // Fallback: sanitize and use as-is
  console.warn(`Warning: Unexpected version format: ${hatchVersion}`);
  return hatchVersion.replace(/\+.*$/, '').replace(/\.dev/, '-dev');
}

/**
 * Update package.json with new version
 */
function updatePackageJson(newVersion) {
  try {
    const packageContent = readFileSync(PACKAGE_JSON, 'utf8');
    const packageData = JSON.parse(packageContent);

    const oldVersion = packageData.version;
    packageData.version = newVersion;

    const updatedContent = JSON.stringify(packageData, null, 2) + '\n';
    writeFileSync(PACKAGE_JSON, updatedContent);

    console.log(`Version updated: ${oldVersion} -> ${newVersion}`);
    return { oldVersion, newVersion };
  } catch (error) {
    throw new Error(`Failed to update package.json: ${error.message}`);
  }
}

/**
 * Generate version TypeScript file for runtime use
 */
function generateVersionFile(version, hatchVersion) {
  const VERSION_TS_FILE = join(DASHBOARD_ROOT, 'src', 'lib', 'version.ts');

  const content = `// Auto-generated version file - DO NOT EDIT MANUALLY
// Generated by scripts/sync-version.js

export const VERSION = '${version}';
export const HATCH_VERSION = '${hatchVersion}';
export const IS_DEVELOPMENT = ${version.includes('dev')};
export const BUILD_TYPE = '${getBuildTypeFromVersion(version)}';
export const GENERATED_AT = '${new Date().toISOString()}';

/**
 * Get the current dashboard version
 */
export function getVersion(): string {
  return VERSION;
}

/**
 * Get a short version (without pre-release identifiers)
 */
export function getShortVersion(): string {
  const match = VERSION.match(/^(\\d+\\.\\d+\\.\\d+)/);
  return match ? match[1] : VERSION;
}

/**
 * Check if this is a development version
 */
export function isDevelopmentVersion(): boolean {
  return IS_DEVELOPMENT;
}

/**
 * Get the build type (release, dev, rc, etc.)
 */
export function getBuildType(): string {
  return BUILD_TYPE;
}

/**
 * Format version for display
 */
export function formatVersionForDisplay(includeType: boolean = false): string {
  if (!includeType || BUILD_TYPE === 'release') {
    return \`v\${VERSION}\`;
  }

  return \`v\${VERSION} (\${BUILD_TYPE})\`;
}
`;

  try {
    writeFileSync(VERSION_TS_FILE, content);
    console.log(`Generated version file: ${VERSION_TS_FILE}`);
  } catch (error) {
    throw new Error(`Failed to generate version file: ${error.message}`);
  }
}

/**
 * Helper to get build type from version string
 */
function getBuildTypeFromVersion(version) {
  if (version.includes('dev')) return 'development';
  if (version.includes('rc')) return 'release candidate';
  if (version.includes('alpha')) return 'alpha';
  if (version.includes('beta')) return 'beta';
  return 'release';
}

/**
 * Main function
 */
function main() {
  try {
    console.log('Synchronizing dashboard version with parent project...');

    // Extract version from parent project
    const hatchVersion = extractVersionFromPython();
    console.log(`Parent project version: ${hatchVersion}`);

    // Format for NPM
    const npmVersion = formatForNpm(hatchVersion);
    console.log(`Formatted for NPM: ${npmVersion}`);

    // Update package.json
    const result = updatePackageJson(npmVersion);

    // Generate version TypeScript file for runtime use
    generateVersionFile(npmVersion, hatchVersion);

    console.log('Version synchronization completed successfully.');

    // Exit with success
    process.exit(0);

  } catch (error) {
    console.error('Error synchronizing version:', error.message);
    process.exit(1);
  }
}

// Run if called directly
if (import.meta.url === `file://${process.argv[1]}`) {
  main();
}

export { extractVersionFromPython, formatForNpm, updatePackageJson };
